Volumetric Weight
<br><br>
 

Shipping costs can be affected by the amount of space a consignment occupies, rather than the actual weight. This is the volumetric (or dimensional) weight. The volumetric weight of a shipment is a calculation that reflects how dense a package is. A low density package generally occupies a greater volume of space, in comparison to its actual weight.
<br><br>
 

The volumetric or dimensional weight is calculated and compared with the actual weight of the shipment to ascertain which is greater; shipping costs are calculated based on the higher weight.
<br><br>
 

The formula used to determine volumetric weight for our consignments is given below:
<br><br>
 

Volumetric Weight(kg) = Length(cm) x Width(cm) x Height(cm) / 5000 (Volumetric Divisor)
